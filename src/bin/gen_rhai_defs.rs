//! gen_rhai_defs: emit a Rhai LSP definition file (.d.rhai) for the
//! conchordal scripting API. Run with:
//!
//!     cargo run --bin gen_rhai_defs > rhai-defs/conchordal.d.rhai
//!
//! Source of truth is `ScriptHost::create_engine`. Regenerate whenever a
//! new `register_fn` lands so editor support stays in sync.

use std::sync::{Arc, Mutex};

use conchordal::life::scripting::{ScriptContext, ScriptHost};

const BUILTIN_BUSES: &[&str] = &["field", "presentation"];

/// Rhai reserved keywords that cannot appear as fn names in a `.d.rhai`.
/// See https://rhai.rs/book/language/keywords.html.
const RHAI_RESERVED: &[&str] = &[
    "var",
    "static",
    "is",
    "goto",
    "exit",
    "match",
    "case",
    "public",
    "protected",
    "new",
    "use",
    "with",
    "module",
    "package",
    "super",
    "spawn",
    "thread",
    "go",
    "sync",
    "async",
    "await",
    "yield",
    "default",
    "void",
    "null",
    "nil",
];

fn main() {
    let ctx = Arc::new(Mutex::new(ScriptContext::default()));
    let engine = ScriptHost::create_engine(ctx);

    println!("// Auto-generated by `cargo run --bin gen_rhai_defs`. DO NOT EDIT.");
    println!("// Source of truth: src/life/scripting/engine.rs");
    println!();
    println!("module static;");
    println!();
    println!("// Built-in buses (registered as module constants).");
    for bus in BUILTIN_BUSES {
        println!("const {bus}: Bus;");
    }
    println!();

    let mut decls: Vec<String> = Vec::new();
    let mut skipped: Vec<String> = Vec::new();
    for sig in engine.gen_fn_signatures(false) {
        let name_end = sig.find('(').expect("signature has '('");
        let name = &sig[..name_end];
        if RHAI_RESERVED.contains(&name) || !is_rhai_identifier(name) {
            skipped.push(sig);
            continue;
        }
        decls.push(format_signature(sig));
    }
    decls.sort();
    for d in decls {
        println!("{d}");
    }

    if !skipped.is_empty() {
        println!();
        println!("// Skipped (reserved keyword or non-identifier function name):");
        skipped.sort();
        for s in skipped {
            println!("// - {s}");
        }
    }
}

fn is_rhai_identifier(name: &str) -> bool {
    let mut chars = name.chars();
    let Some(first) = chars.next() else {
        return false;
    };
    if !(first == '_' || first.is_ascii_alphabetic()) {
        return false;
    }
    chars.all(|c| c == '_' || c.is_ascii_alphanumeric())
}

/// Convert one `gen_fn_signatures` line to a `.d.rhai` fn declaration.
///
/// Input: `name(_: T1, _: T2) -> R`  (R optional)
/// Output: `fn name(arg0: T1', arg1: T2') -> R';`
fn format_signature(sig: String) -> String {
    let (head, ret) = match sig.split_once(" -> ") {
        Some((h, r)) => (h.to_string(), Some(r.to_string())),
        None => (sig, None),
    };

    let open = head.find('(').expect("signature has '('");
    let close = head.rfind(')').expect("signature has ')'");
    let name = &head[..open];
    let params_inside = &head[open + 1..close];

    let params: Vec<String> = if params_inside.is_empty() {
        Vec::new()
    } else {
        params_inside
            .split(", ")
            .enumerate()
            .map(|(i, p)| {
                let ty = p.trim_start_matches("_: ");
                format!("arg{i}: {}", map_type(ty))
            })
            .collect()
    };

    let params_joined = params.join(", ");
    match ret {
        Some(r) => {
            let mapped = map_type(&r);
            if mapped == "()" {
                format!("fn {name}({params_joined});")
            } else {
                format!("fn {name}({params_joined}) -> {mapped};")
            }
        }
        None => format!("fn {name}({params_joined});"),
    }
}

/// Map a Rust type string from `gen_fn_signatures` to a `.d.rhai` type name.
/// Mapping rules follow the conventions seen in rhai-lsp's example
/// definition files (e.g. `static.d.rhai`).
fn map_type(t: &str) -> String {
    let t = t.trim();
    if let Some(inner) = unwrap_result(t) {
        return map_type(&inner);
    }
    // Collapse module paths: `conchordal::life::scripting::GroupHandle` -> `GroupHandle`,
    // `types::dynamic::Dynamic` -> `Dynamic`.
    let last = t.rsplit("::").next().unwrap_or(t).trim();
    match last {
        "string" => "String".to_string(),
        "Dynamic" => "?".to_string(),
        "array" => "[?]".to_string(),
        "SpeciesHandle" => "Material".to_string(),
        "GroupHandle" => "Participant".to_string(),
        // f64, i64, bool, (), Fn, custom handle types -> pass through.
        other => other.to_string(),
    }
}

/// Strip `core::result::Result<T, ...>` and return T. Returns None if the
/// input isn't a Result wrapper.
fn unwrap_result(t: &str) -> Option<String> {
    let prefix = "core::result::Result<";
    let rest = t.strip_prefix(prefix)?;
    let mut depth: i32 = 0;
    for (i, c) in rest.char_indices() {
        match c {
            '<' => depth += 1,
            '>' => {
                if depth == 0 {
                    return Some(rest[..i].to_string());
                }
                depth -= 1;
            }
            ',' if depth == 0 => return Some(rest[..i].to_string()),
            _ => {}
        }
    }
    None
}
